package com.example.tmizzle2005.myriadChallenge.Model;

/**
 * Created by tmizzle2005 on 3/15/15.
 * this class holds the information of kingdom's item
 */
public class KingdomItem {
    private int id;
    private String name;
    private String image;

    /**
     * this method returns the kingdoms'id
     * @return the kingdom's id
     */
    public int getId() {
        return id;
    }

    /**
     * this method returns the kingdom''s name
     * @return the kingdom's name
     */
    public String getName() {
        return name;
    }

    /**
     * this method returns the kingdom's image
     * @return the kingdom's image
     */
    public String getImage() {
        return image;
    }

}
